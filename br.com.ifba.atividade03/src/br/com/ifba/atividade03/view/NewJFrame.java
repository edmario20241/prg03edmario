    private void btnVerIdadeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVerIdadeActionPerformed
        // TODO add your handling code here:
        
    // Pegar os anos dos campos de texto
        String anoNasc = txtDataNascimento.getText();
        String anoAtual = txtDataAtual.getText();
    
    // Converter para n√∫meros
        int nasc = Integer.parseInt(anoNasc);
        int atual = Integer.parseInt(anoAtual);
    
    // Calcular a idade
        int idade = atual - nasc;
    
    // Mostrar o resultado
        lblResultado.setText(idade + " anos");
    }//GEN-LAST:event_btnVerIdadeActionPerformed

